<script lang="ts">
  import { viewModel, graphStore } from '../lib/stores/graphStore';

  function handleChange(event: Event) {
    const target = event.target as HTMLSelectElement;
    graphStore.onSourceChange(target.value);
  }
</script>

<select
  id="snapshot-source"
  class="bg-input text-foreground border-0 rounded px-2 py-1 text-xs focus:outline-none focus:ring-1 focus:ring-primary transition-all cursor-pointer"
  value={$viewModel.sourceValue}
  onchange={handleChange}
>
  {#each $viewModel.sourceOptions as option}
    <option value={option.value}>{option.text}</option>
  {/each}
</select>
